package it.hackcaffebabe.jxswingplus.timer;

/**
 * This enumeration describe the state of
 * {@link it.hackcaffebabe.jxswingplus.timer.JXTimer}
 *  
 * @author Andrea Ghizzoni. More info at andrea.ghz@gmail.com
 * @version 1.0
 */
public enum JXTimerState
{
	/** Indicate that the {@link JXTimer} is running */
	RUNNING,
	/** Indicate that the {@link JXTimer} is in pause */
	PAUSED,
	/** Indicate that the {@link JXTimer} is stopped */
	STOPPED
}
